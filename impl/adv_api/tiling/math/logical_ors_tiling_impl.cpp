#include "graph/tensor.h"

namespace AscendC {
namespace {
static constexpr uint32_t LOGICAL_ORS_UINT8_SIZE = 1;
static constexpr uint32_t LOGICAL_ORS_UINT16_SIZE = 2;
static constexpr uint32_t LOGICAL_ORS_UINT32_SIZE = 4;
static constexpr uint32_t LOGICAL_ORS_UINT64_SIZE = 8;
static const std::set<uint32_t> SUPPORT_TYPESIZE = { LOGICAL_ORS_UINT8_SIZE, LOGICAL_ORS_UINT16_SIZE, LOGICAL_ORS_UINT32_SIZE, LOGICAL_ORS_UINT64_SIZE};
static constexpr const char LOGICAL_ORS_GET_MAX_MIN[] = "GetLogicalOrsMaxMinTmpSize";
static constexpr const char LOGICAL_ORS_GET_TMP_BUFFER[] = "GetLogicalOrsTmpBufferFactorSize";
} // namespace

void GetLogicalOrsMaxMinTmpSize(const platform_ascendc::PlatformAscendC& ascendcPlatform, const ge::Shape& srcShape, 
    const uint32_t typeSize, const bool isReuseSource, uint32_t& maxValue, uint32_t& minValue)
{
    HighLevelApiCheck::SrcShapeSizeVerifyingParameters<LOGICAL_ORS_GET_MAX_MIN>(srcShape.GetShapeSize(), typeSize);
    HighLevelApiCheck::TypeSizeVerifyingParameters<LOGICAL_ORS_GET_MAX_MIN>(typeSize, SUPPORT_TYPESIZE);
    HighLevelApiCheck::IsReuseSourceVerifyingParameters<LOGICAL_ORS_GET_MAX_MIN>(isReuseSource);
    platform_ascendc::SocVersion socVersion = ascendcPlatform.GetSocVersion();

    ASCENDC_HOST_ASSERT((socVersion == platform_ascendc::SocVersion::ASCEND910_95 ||
        socVersion == platform_ascendc::SocVersion::ASCEND910_55),
        return, "Unsupported SocVersion of LogicalOrs API.");
    maxValue = 0u;
    minValue = 0u;
}

void GetLogicalOrsTmpBufferFactorSize(const platform_ascendc::PlatformAscendC& ascendcPlatform, const uint32_t typeSize, 
    uint32_t& maxLivedNodeCount, uint32_t& extraBuf)
{
    HighLevelApiCheck::TypeSizeVerifyingParameters<LOGICAL_ORS_GET_TMP_BUFFER>(typeSize, SUPPORT_TYPESIZE);
    platform_ascendc::SocVersion socVersion = ascendcPlatform.GetSocVersion();
    ASCENDC_HOST_ASSERT((socVersion == platform_ascendc::SocVersion::ASCEND910_95 ||
        socVersion == platform_ascendc::SocVersion::ASCEND910_55),
        return, "Unsupported SocVersion of LogicalOrs API.");
    extraBuf = 0u;
    maxLivedNodeCount = 0u;
}
} // namespace AscendC
